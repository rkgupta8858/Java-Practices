class DataType
{
	public static void main(String args[])
	{
		byte b = 100;
		System.out.println("Byte Value is : " +b);
		
		char c = 'C';
		System.out.println("Character value is : " +c);
		
		int i = 5456625;
		System.out.println("Integer value is : "+i);
		
		short s = 1111;
		System.out.println("Short value is : "+s);
	}
	
}